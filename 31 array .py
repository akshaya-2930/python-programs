# Sample array
data = np.array([12, 15, 7, 10, 18, 21, 13])

# Calculations
mean = np.mean(data)
median = np.median(data)
std_dev = np.std(data)

# Results
print("Array:", data)
print("Mean:", mean)
print("Median:", median)
print("Standard Deviation:", std_dev)
